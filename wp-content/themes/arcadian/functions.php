function css_js_load() {
	wp_enqueue_script('vendor', get_template_directory_uri() . '/js/vendor.js', '', '', 'in_footer');
	wp_enqueue_script('custom', get_template_directory_uri() . '/js/common.js', '', '', 'in_footer');
	
	if (is_front_page()) {
		wp_enqueue_script('index', get_template_directory_uri() . '/js/index.js', '', '', 'in_footer');
	}
	
	if (is_category('portfolio')) {
		wp_enqueue_script('portfolio', get_template_directory_uri() . '/js/portfolio.js', '', '', 'in_footer');
	}
	
	wp_enqueue_style('vendor', get_template_directory_uri().'/css/vendor.css');
	wp_enqueue_style('style', get_template_directory_uri().'/css/style.css');
	wp_enqueue_style('theme-style', get_stylesheet_uri());
}
add_action('wp_enqueue_scripts', 'css_js_load');

